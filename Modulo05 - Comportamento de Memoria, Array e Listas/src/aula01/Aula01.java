package aula01;

public class Aula01 {

	public static void main(String[] args) {
		//Aula 01 - Tipos Referência vs. Tipo Valor
		
		int p;
		System.out.println(p); // Dá erro porque a variavel P nao foi inicializada
		
		int c = 10;
		System.out.println(c); //Aqui nao da erro porque a variavel foi inicializada

	}

}
